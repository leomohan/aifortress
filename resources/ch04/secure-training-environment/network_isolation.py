"""
network_isolation.py  —  Egress allow-list and network policy generator
AI Fortress · Chapter 4 · Code Sample 4.A

Generates network restriction artefacts for training jobs:

  1. iptables rules  — per-job egress allow-list for bare-metal / VM environments
  2. nftables rules  — modern alternative to iptables
  3. Kubernetes NetworkPolicy manifest — restricts pod egress to declared endpoints
  4. Docker --network run flags  — for single-container training jobs
  5. Audit log verifier  — scans system network audit logs for unexpected connections
     that occurred during a completed training job (post-hoc analysis)

Principle: deny-all egress by default, then allow only:
  - Dataset bucket endpoints (S3, GCS, Azure Blob)
  - Model registry (MLflow, W&B, Vertex)
  - Package mirrors (PyPI, conda)
  - Internal secrets backend (Vault, AWS Secrets Manager)
"""
from __future__ import annotations

import json
import re
import socket
from dataclasses import dataclass, field
from typing import List, Optional


@dataclass
class AllowedEndpoint:
    host:        str
    port:        int
    protocol:    str = "tcp"    # tcp | udp
    description: str = ""

    def resolve_ip(self) -> Optional[str]:
        try:
            return socket.gethostbyname(self.host)
        except socket.gaierror:
            return None


@dataclass
class NetworkPolicy:
    job_id:    str
    endpoints: List[AllowedEndpoint] = field(default_factory=list)

    def add(self, host: str, port: int, protocol: str = "tcp", description: str = "") -> None:
        self.endpoints.append(AllowedEndpoint(host, port, protocol, description))


